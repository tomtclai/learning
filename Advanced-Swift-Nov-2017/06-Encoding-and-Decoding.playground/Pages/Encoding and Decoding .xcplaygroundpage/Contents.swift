# Encoding and Decoding 

Serializing a program's internal data structures into some kind of data
interchange format and vice versa is one of the most common programming tasks.
Swift calls these operations *encoding* and *decoding*. One of the headline
features of Swift 4 is a standardized design for encoding and decoding data that
all custom types can opt into.

