# Built-In Collections 

Collections of elements are among the most important data types in any
programming language. Good language support for different kinds of containers
has a big impact on programmer productivity and happiness. Swift places special
emphasis on sequences and collections — so much of the standard library is
dedicated to this topic that we sometimes have the feeling it deals with little
else. The resulting model is way more extensible than what you may be used to
from other languages, but it's also quite complex.

In this chapter, we're going to take a look at the major collection types Swift
ships with, with a focus on how to work with them effectively and idiomatically.
In the next chapter, we'll climb up the abstraction ladder and see how the
collection protocols in the standard library work.

