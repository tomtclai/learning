## Functions as Delegates

Delegates. They're everywhere. Drummed into the heads of Objective-C (and Java)
programmers is this message: use protocols (interfaces) for callbacks. You
define a protocol, your owner implements that protocol, and it registers itself
as your delegate so that it gets callbacks.

If a delegate protocol contains only a single method, you can mechanically
replace the property storing the delegate object with one that stores the
callback function directly. However, there are a number of tradeoffs to keep in
mind.

