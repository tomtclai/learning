## Who Is This Book For?

This book targets experienced (though not necessarily expert) programmers — such
as existing Apple-platform developers, or those coming from other languages such
as Java or C++ — who want to bring their knowledge of Swift to the same level as
that of Objective-C or some other language. It's also suitable for new
programmers who started on Swift, have grown familiar with the basics, and are
looking to take things to the next level.

The book isn't meant to be an introduction to Swift; it assumes you're familiar
with the syntax and structure of the language. If you want some good, compact
coverage of the basics of Swift, the best source is the official Apple Swift
book (available on
[iBooks](https://itunes.apple.com/book/the-swift-programming-language-swift-4/id881256329?mt=11)
or on [Apple's
website](https://developer.apple.com/library/content/documentation/Swift/Conceptual/Swift_Programming_Language)).
If you're already a confident programmer, you could try reading our book and the
Apple Swift book in parallel.

This is also not a book about programming for macOS or iOS devices. Of course,
since Swift is currently mainly used on Apple platforms, we've tried to include
examples of practical use, but we hope this book will be useful for
non-Apple-platform programmers as well. The vast majority of the examples in the
book should run unchanged on other operating systems. The ones that don't are
either fundamentally tied to Apple's platforms (because they use iOS frameworks
or rely on the Objective-C runtime) or only require minimal changes, such as
replacing the BSD-specific function we use to generate random numbers with a
Linux equivalent.

