## Further Reading

One way to further hone your functional programming skills is by learning
Haskell. There are many other functional languages, such as F\#, OCaml, Standard
ML, Scala, or Racket, each of which would make a fine choice of language to
complement Swift. Haskell, however, is the most likely to challenge your
preconceptions about programming. Learning to program well in Haskell will
change the way you work in Swift.

There are a lot of Haskell books and courses available these days. Graham
Hutton's *Programming in Haskell* \[-@hutton\] is a great starting point to
familiarize yourself with the language basics. [*Learn You a Haskell for Great
Good\!*](http://learnyouahaskell.com/) is free to read online and covers some
more advanced topics. [*Real World Haskell*](http://book.realworldhaskell.org/)
describes several larger case studies and a lot of the technology missing from
many other books, including support for profiling, debugging, and automated
testing. Richard Bird is famous for his "functional pearls" — elegant,
instructive examples of functional programming, many of which can be found in
his book, *Pearls of Functional Algorithm Design* \[-@bird\], or
[online](http://www.haskell.org/haskellwiki/Research_papers/Functional_pearls).
Finally, *The Fun of Programming* is a collection of domain-specific languages
embedded in Haskell, covering domains ranging from financial contracts to
hardware design \[@fop\].

If you want to learn more about programming language design in general, Benjamin
Pierce's *Types and Programming Languages* \[-@tapl\] is an obvious choice. Bob
Harper's *Practical Foundations for Programming Languages* \[-@harper\] is more
recent and more rigorous, but unless you have a solid background in computer
science or mathematics, you may find it hard going.

Don't feel obliged to make use of all of these resources; many of them may not
be of interest to you. But you should be aware that there is a huge amount of
work on programming language design, functional programming, and mathematics
that has directly influenced the design of Swift.

If you're interested in further developing your Swift skills – not only the
functional parts of it – we've written an entire [book about advanced swift
topics](https://objc.io/books/advanced-swift), covering topics low-level
programming to high-level abstractions.

