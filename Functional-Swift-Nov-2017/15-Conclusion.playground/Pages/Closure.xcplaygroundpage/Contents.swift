## Closure

This is an exciting time for Swift. The language is still very much in its
infancy. Compared to Objective-C, there are many new features — borrowed from
existing functional programming languages — that have the potential to
dramatically change the way we write software for iOS and OS X.

At the same time, it is unclear how the Swift community will develop. Will
people embrace these features? Or will they write the same code in Swift as they
do in Objective-C, but without the semicolons? Time will tell. By writing this
book, we hope to have introduced you to some concepts from functional
programming. It is up to you to put these ideas in practice as we continue to
shape the future of Swift.

