## Why Use Enumerations?

Working with optionals may still be preferable over the `Result` type that we've
defined here, for a variety of reasons: the built-in syntactic sugar can be
convenient; the interface you define will be more familiar to Swift developers,
as you only rely on existing types instead of defining your own enumeration; and
sometimes the `Error` isn't worth the additional hassle of defining an
enumeration.

The point we want to make, however, isn't that the `Result` type is the best way
to handle all errors in Swift. Instead, we hope to illustrate how you can use
enumerations to define your own types tailored to your specific needs. By making
these types precise, you can use Swift's type checking to your advantage and
prevent many bugs before your program has been tested or run.

