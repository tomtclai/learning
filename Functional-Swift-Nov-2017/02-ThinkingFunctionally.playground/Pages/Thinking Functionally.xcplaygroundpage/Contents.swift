# Thinking Functionally

Functions in Swift are *first-class values*, i.e. functions may be passed as
arguments to other functions, and functions may return new functions. This idea
may seem strange if you're used to working with simple types, such as integers,
booleans, or structs. In this chapter, we'll try to explain why first-class
functions are useful and provide our first example of functional programming in
action.

Throughout this chapter, we'll use both functions and methods. Methods are a
special case of functions: they are functions defined on a type.

