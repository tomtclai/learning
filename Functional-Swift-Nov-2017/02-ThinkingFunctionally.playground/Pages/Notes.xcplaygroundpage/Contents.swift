## Notes

The code presented here is inspired by the Haskell solution to a problem posed
by the United States Advanced Research Projects Agency (ARPA) by @hudak-jones.

Objective-C added support for first-class functions with the addition of blocks:
you can use functions and closures as parameters and easily define them inline.
However, working with them isn't nearly as convenient in Objective-C as it is in
Swift, even though they're semantically equivalent.

Historically, the idea of first-class functions can be traced as far back as
Church's lambda calculus \[@church; @barendregt\]. Since then, the concept has
made its way into numerous (functional) programming languages, including
Haskell, OCaml, Standard ML, Scala, and F\#.

