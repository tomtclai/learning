subject. Interested readers may also want to read Ralf Hinze and Ross Paterson's
work on finger trees \[-@hinze-paterson\], which are general-purpose purely
functional data structures with numerous applications. Finally,
[StackOverflow](http://cstheory.stackexchange.com/questions/1539/whats-new-in-purely-functional-data-structures-since-okasaki)
