## Recap

Functions are first-class values in Swift. Treating functions as data can make
our code more flexible. We've seen how we can replace runtime programming with
simple functions. We've compared different ways to implement delegates. We've
looked at `mutating` functions and `inout` parameters, as well as computed
properties (which really are a special kind of function). Finally, we've
discussed the `@autoclosure` and `@escaping` attributes. In the chapters on
