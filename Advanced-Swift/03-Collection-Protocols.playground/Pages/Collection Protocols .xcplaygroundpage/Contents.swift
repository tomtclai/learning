# Collection Protocols 

We saw in the previous chapter that `Array`, `Dictionary`, and `Set` don't exist
in a vacuum. They're all implemented on top of a rich set of abstractions for
processing series of elements that's provided by the Swift standard library.
This chapter is all about the `Sequence` and `Collection` protocols, which form
the cornerstones of this model. We'll cover how these protocols work, why they
work the way they do, and how you can write your own sequences and collections.

