# Interoperability 

One of Swift's greatest strengths is the low friction when interoperating with C
and Objective-C. Swift can automatically bridge Objective-C types to native
Swift types, and it can even bridge with many C types. This allows us to use
existing libraries and provide a nice interface on top.

In this chapter, we'll create a wrapper around the
[CommonMark](http://commonmark.org) library. CommonMark is a formal
specification for Markdown, a popular syntax for formatting plain text. If
you've ever written a post on GitHub or Stack Overflow, you've probably used
Markdown. After this practical example, we'll take a look at the tools the
standard library provides for working with memory, and we'll see how they can be
used to interact with C code.

